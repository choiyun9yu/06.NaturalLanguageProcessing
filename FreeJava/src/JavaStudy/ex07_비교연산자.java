package JavaStudy;

public class ex07_비교연산자 {
	
	public static void main (String[] args) {
		int i = 10;
		int j = 10;
		
		System.out.println(i == j );
		System.out.println(i != j );
		System.out.println(i < j);
		System.out.println(i <= j);
		System.out.println(i > j);
		System.out.println(i >= j);
		
		i += 10;
		
		System.out.println(i);
	}
}
