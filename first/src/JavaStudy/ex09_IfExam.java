package JavaStudy;

public class ex09_IfExam {

	public static void main(String[] args) {
		int x = 50;
		int y = 50;
		
		if (x < y) {
			System.out.println("x는 y보다 작습니다.");
		}
		
		if (x == y) {
			System.out.println("x는 y와 같습니다.");
		}
		
		if (x > y) {
			System.out.println("x는 y보다 작습니다.");
		}

	}

}
