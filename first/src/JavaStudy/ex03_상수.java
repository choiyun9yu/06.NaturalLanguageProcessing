package JavaStudy;

public class ex03_상수 {

	public static void main(String[] args) {
//		상수는 한번 저장한 데이터 값을 바꿀 수 없다. (재할당 불가)
//		상수는 선언할 때 final을 붙여주면 된다.
//		상수는 모두 대문자로된 것으로 구성하는 것이 관례이다.
		final int J;
		J = 10;
		System.out.println(J);
		
/** 상수 사용례
 	1) 중간에 재선언 되어서 바꾸면 안되느 경우 
 	2) 여러번 중복되서 사용되는 경우 (정의하는 부분만 바꿔서 간편하게 수정)
 */
		
	}

}
